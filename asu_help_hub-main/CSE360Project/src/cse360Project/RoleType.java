package cse360Project;

/**
 * Enum representing different user roles within the CSE360 Help System.
 */
public enum RoleType {
    ADMIN,        // Administrator with full access to all system functionalities
    STUDENT,      // Student with access to course materials and submission features
    INSTRUCTOR,   // Instructor with abilities to create courses, grade assignments, etc.
    IA,           // Instructional Assistant assisting instructors and students
    PROFESSOR     // Professor with advanced privileges, possibly overseeing multiple courses
}
